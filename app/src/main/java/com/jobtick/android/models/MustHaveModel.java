package com.jobtick.android.models;

public class MustHaveModel  {

    String mustHaveTitle;
    boolean isChecked;

    public String getMustHaveTitle() {
        return mustHaveTitle;
    }

    public void setMustHaveTitle(String mustHaveTitle) {
        this.mustHaveTitle = mustHaveTitle;
    }

    public boolean isChecked() {
        return isChecked;
    }

    public void setChecked(boolean checked) {
        isChecked = checked;
    }
}
